body(
    '最直观的方法是统计每条通路中包含多少个疾病关联基因——重叠基因越多，通路似乎越重要。'
    '然而，这种简单计数存在两个明显的缺陷。第一，大通路天然包含更多基因，仅凭基因数量无法公平地比较'
    '大通路和小通路的相对重要性。第二，也是更本质的问题，并非所有疾病基因都同等重要：'
    '有些基因在所有样本中表达水平几乎不变（即所谓的"管家基因"），它们虽然与疾病有关联，'
    '但可能并非驱动疾病异质性的关键因素；另一些基因在不同样本间表达差异极大，'
    '这种高变异性往往暗示着它们在疾病的不同亚型、不同分期或不同个体中发挥着差异化的功能。')

body(
    '为了区分这两类基因，本系统引入了表达变异系数（Coefficient of Variation, CV）作为加权因子。'
    '变异系数定义为基因表达值的标准差与均值之比，它衡量的是表达值相对于其均值的离散程度。'
    'CV值高的基因意味着其表达水平在不同样本间波动剧烈，这种波动可能反映了疾病状态、'
    '肿瘤微环境或患者遗传背景的差异，因此具有更高的生物学信息量。')

body(
    '基于上述考虑，通路影响力评分被定义为两个因子的乘积：通路与疾病的基因重叠数量，'
    '以及这些重叠基因的平均CV值。形式化地，对于疾病d和通路p，评分公式为：')

formula('PathwayScore(p, d) = |Gp ∩ Gd| × mean(CV(g)),  g ∈ Gp ∩ Gd')

body(
    '其中Gp表示通路p中的全部基因，Gd表示与疾病d关联的基因集合，CV(g)为基因g的表达变异系数。'
    '变异系数的具体计算中，为了避免低表达基因因均值接近零而产生不稳定的极大CV值，'
    '在分母中加入了一个极小的常数ε作为正则化项。')

body(
    '这一评分方法的优势在于它同时捕获了两个维度的信息。第一个维度是通路与疾病在基因组成上的重叠程度，'
    '反映的是通路与疾病之间的"结构性关联"；第二个维度是重叠基因的表达变异程度，'
    '反映的是这些基因在患者群体中的"功能性异质性"。当一条通路不仅包含大量疾病基因，'
    '而且这些基因在不同患者中呈现高度差异化的表达模式时，我们有理由认为这条通路在疾病的异质性中扮演着关键角色。')

